package kr.or.connect.reservation.dto;

public class FileInfo {

	private int id;
	private String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	@Override
	public String toString() {
		return "FileInfo [id=" + id + ", name=" + name + "]";
	}
	
}
